#include "../includes/push_swap.h"

void	ft_putnbr(int n)
{
	int	x;

	x = 1;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	while (n / x > 9)
		x *= 10;
	while (x)
	{
		ft_putchar((n / x) + 48);
		n %= x;
		x /= 10;
	}
}
