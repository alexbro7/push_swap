int	ft_is_num(char *s, int *i)
{
	if (s[*i] == '-')
		*i += 1;
	if (s[*i] < '0' || s[*i] > '9')
		return (0);
	while (s[*i] >= '0' && s[*i] <= '9')
		*i += 1;
	return (1);
}
