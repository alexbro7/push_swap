#include "../includes/push_swap.h"

int	ft_is_sort(t_pile a)
{
	int		i;

	i = 0;
	if (a.s < 2)
		return (1);
	if (a.p[i].l > a.p[i + 1].l)
		return (0);
	while (i < a.s - 1)
	{
		if (a.p[i].l > a.p[i + 1].l)
			return (0);
		i++;
	}
	return (1);
}
